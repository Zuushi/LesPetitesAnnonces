    // Methode qui met à jour le profil
    private void jButton3ActionPerformed(ActionEvent evt, User A) {
    if(jTextField5.getText().isEmpty() || jTextField6.getText().isEmpty() || jTextField7.getText().isEmpty() ){
            JOptionPane d = new JOptionPane();
            d.showMessageDialog( this, "Veuillez remplir tous les champs.");
    }else{
            Connection cnx = null;
            PreparedStatement checkmail = null;

            try{	
                    cnx = Connect.Connecter();
                    String erreur = "";

                    if(jTextField5.getText().length() < 3){
                            erreur = erreur + "Veuillez saisir un nom valide d'au moins 3 caracteres. \n";
                    }
                    if(jTextField6.getText().length() < 3){
                            erreur = erreur + "Veuillez saisir un prenom valide d'au moins 3 caracteres. \n";
                    }		

                    if(jTextField7.getText().length() < 10){
                            erreur = erreur + "Veuillez rentrer un numero de telephone valide d'au moins 10 caracteres. \n";
                    }


                    if(erreur.equals("")){
                            A.modifierProfil( jTextField5.getText(), jTextField6.getText(), jTextField7.getText());
                            JOptionPane.showMessageDialog(null, "Mise à jour réussie !");
                    }else{
                            JOptionPane.showMessageDialog(null, "Veuillez remplir correctement les champs : \n" + erreur);
                    }
            }catch(Exception e){
                    JOptionPane.showMessageDialog(null, "Probleme lors de la modification : " + e);

            }finally{
                    if(cnx != null){
                            try {
                                    cnx.close();
                            } catch (SQLException e) {
                                    // TODO Auto-generated catch block
                                    e.printStackTrace();
                            }
                    }
                    if(checkmail != null){
                            try {
                                    checkmail.close();
                            } catch (SQLException e) {
                                    // TODO Auto-generated catch block
                                    e.printStackTrace();
                            }
                    }
            }
        }
    }
    
