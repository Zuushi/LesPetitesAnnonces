    private void jButton4ActionPerformed(ActionEvent evt, User A) {
        // on recupère les mdp saisis
        String pw = new String(jPasswordField3.getPassword());
        String vpw = new String(jPasswordField2.getPassword());
        System.out.println(pw);
        System.out.println(vpw);
        
        if (pw.length() > 0 || vpw.length() > 0) {
            if(pw.equals(vpw)) {
                try {
                    pw = hashPassword(pw);
                    A.modifierMdp(pw);
                    JOptionPane.showMessageDialog(null, "Changement de mot de passe réussi!");
                } catch (SQLException ex) {
                    Logger.getLogger(ModifProfil.class.getName()).log(Level.SEVERE, null, ex);
                }
            } else {
                JOptionPane.showMessageDialog(null, "Les mots de passes ne correspondent pas !");
            }
        } else {
            JOptionPane.showMessageDialog(null, "Veuillez saisir un mot de passe !");
        }
    }
